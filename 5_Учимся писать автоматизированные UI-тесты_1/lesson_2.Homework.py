from selenium import webdriver  # Импортируем библиотеку Selenium, которая позволяет управлять браузером программно
from selenium.webdriver.common.by import By  # Импортируем enum By для указания способа поиска элементов (например, по ID, классу, тексту и т. д.)
from selenium.webdriver.support.ui import WebDriverWait  # Импортируем класс WebDriverWait для ожидания появления элементов на странице
from selenium.webdriver.support import expected_conditions as EC  # Импортируем модуль expected_conditions с условиями ожидания (например, элемент стал кликабелен)
import time
# Инициализация драйвера Chrome
driver = webdriver.Chrome()  # Создаём экземпляр драйвера Chrome. Если ChromeDriver добавлен в PATH системы, достаточно указать просто Chrome().
# Если ChromeDriver не в PATH, нужно указать полный путь к файлу chromedriver, например: webdriver.Chrome('/путь/к/chromedriver')

try:  # Начинаем блок try — он нужен для обработки исключений (ошибок). Если код внутри try выполнится без ошибок, блок finally выполнится в любом случае
    # Открытие страницы с динамическими ID
    driver.get('http://uitestingplayground.com/dynamicid')  # Открываем указанную веб-страницу в браузере. Метод get() ждёт полной загрузки страницы
    driver.implicitly_wait(10)  # Устанавливаем неявное ожидание (implicit wait) в 10 секунд. Это значит, что Selenium будет ждать до 10 секунд перед тем, как выдать ошибку,
    # если элемент не найден. Это ожидание применяется ко всем последующим поискам элементов на странице.

    # Поиск синей кнопки по CSS-классу (без использования ID)
    # Обычно синие кнопки имеют класс "btn-primary" или похожий
    blue_button = WebDriverWait(driver, 10).until(  # Используем явное ожидание (explicit wait) — ждём до 10 секунд, пока элемент не станет доступен для клика
        EC.element_to_be_clickable((By.CSS_SELECTOR, ".btn-primary"))  # Указываем условие ожидания: элемент с CSS-селектором ".btn-primary" должен стать кликабелен.
        # By.CSS_SELECTOR — способ поиска элемента по CSS-селектору, ".btn-primary" — класс кнопки (обычно используется для синих кнопок в Bootstrap)
    )

    # Кликаем по кнопке
    blue_button.click()  # Выполняем клик по найденной кнопке. Метод click() симулирует нажатие мыши на элемент
    print("Синяя кнопка нажата!")  # Выводим сообщение в консоль, подтверждающее, что клик выполнен

    time.sleep(5) 

finally:  # Блок finally выполняется в любом случае — даже если в блоке try возникла ошибка. Это гарантирует выполнение кода внутри finally
    # Закрываем браузер после выполнения
    driver.quit()  # Закрываем браузер и завершаем работу драйвера. Это важно для освобождения системных ресурсов
